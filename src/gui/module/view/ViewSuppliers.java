    private void searchSuppliers(String search){
        SelectProcess selectProcess = new SelectProcess();
        ResultSet rs = selectProcess.searchSuppliers(search);
        tblSuppliers.setModel(DbUtils.resultSetToTableModel(rs));
        
        try {
            rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(ViewSuppliers.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        int width = tblSuppliers.getWidth();
        tblSuppliers.getColumnModel().getColumn(0).setPreferredWidth((int) (width * .2));
        tblSuppliers.getColumnModel().getColumn(1).setPreferredWidth((int) (width * .8));                
    }
