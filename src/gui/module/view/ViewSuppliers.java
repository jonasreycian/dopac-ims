    private void btnSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSelectActionPerformed
        int row = tblSuppliers.getSelectedRow();
        if(row >= 0){
            int id = Integer.valueOf(tblSuppliers.getValueAt(row, 0).toString());
            suppliersEntity = new SuppliersEntity(id);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Please select an item in the table.", "INFORMATION MESSAGE", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnSelectActionPerformed

