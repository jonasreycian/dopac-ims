    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = tfChemicalName.getText();
        String casrn = tfCasrn.getText();
        String msds = tfMSDS.getText().length()>0? Random.getFuckingRandomString() + ".pdf" : "";
        String formula = tfFormula.getText();
        String type1 = cbType1.getSelectedItem().toString();
        String type2 = cbType2.getSelectedItem().toString();
        
        if(name.isEmpty() && casrn.isEmpty()){
            JOptionPane.showMessageDialog(this, "Chemical name and casrn must not be empty", "Error message!", JOptionPane.ERROR_MESSAGE);
        }
        else{
            InsertProcess insertProcess = new InsertProcess();
            boolean status = insertProcess.insertChemical(name, casrn, formula, msds, type1, type2); // return false if successful

            if(!status){
                JOptionPane.showMessageDialog(this, "A new chemical has been added successfully", "Success!", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Failed to add!", "Error message!", JOptionPane.ERROR_MESSAGE);
                this.dispose();
            }

            if(msdsFile != null){
                try {
                    FileUtils.copyFile(msdsFile, new File(System.getProperty("user.dir") + "\\pdfs\\" + msds));

                } catch (IOException ex) {
                    Logger.getLogger(AddChemical.class.getName()).log(Level.SEVERE, null, ex);
                }
            }            
        }
    }//GEN-LAST:event_btnAddActionPerformed

