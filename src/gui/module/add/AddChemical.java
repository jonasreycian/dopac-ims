    private void btnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBrowseActionPerformed
        JFileChooser jfc = new JFileChooser(new File(System.getProperty("user.home")));
        FileNameExtensionFilter ff = new FileNameExtensionFilter("PDF Files", "pdf");
        
        jfc.setMultiSelectionEnabled(false);
        jfc.setDialogTitle("Select pdf file...");
        jfc.setDialogType(JFileChooser.OPEN_DIALOG);
        jfc.setFileFilter(ff);
        
        if (jfc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File sourceFile = jfc.getSelectedFile();

            directory = sourceFile.getAbsolutePath();
            directory = directory.substring(0, directory.lastIndexOf("\\"));
            System.out.println("Path: " + directory);
            tfMSDS.setText(sourceFile.getName());

            msdsFile = sourceFile;
        }
        
    }//GEN-LAST:event_btnBrowseActionPerformed

