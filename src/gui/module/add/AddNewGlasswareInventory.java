    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(lblID.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "Please select a glassware from the table!", "Error message!", JOptionPane.ERROR_MESSAGE);
        }
        else{
            
            
            InsertProcess insertProcess = new InsertProcess();
            
            try {
                int glassware_id = Integer.valueOf(lblID.getText());
                int quantity = Integer.valueOf(tfQuantity.getText());
                String size = tfSize.getText();
                String date_purchased = cbPYear.getSelectedItem() +"-"+parseMonth(cbPMonth.getSelectedIndex()+1)+"-"+ parseMonth(cbPDay.getSelectedIndex()+1);
                
                boolean status = insertProcess.insertGlasswareInventory(glassware_id, size, quantity, date_purchased);
                if(!status){
                    JOptionPane.showMessageDialog(null, "Successfully added!", "Success!", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Failed to add!", "Error message!", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error message!", JOptionPane.ERROR_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_btnAddActionPerformed

