    private void searchGlassware(String keyword) {
        SelectProcess selectProcess = new SelectProcess();
        ResultSet resultSet = selectProcess.searchGlasswares(keyword);
        
        tblGlassware.setModel(DbUtils.resultSetToTableModel(resultSet));
        try {
            resultSet.close();
        } catch (SQLException ex) {
            Logger.getLogger(AddNewGlasswareInventory.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        int width = tblGlassware.getWidth();
        tblGlassware.getColumnModel().getColumn(0).setPreferredWidth((int) (width * .2));
        tblGlassware.getColumnModel().getColumn(1).setPreferredWidth((int) (width * .8));
    }
    
    private void initializeYear(){
        String[] MONTH = {"January", "February", "March", "April", "May", "June", "July", "August", "September", "October", "November", "December"};
        cbPYear.removeAllItems();
        
        Calendar calendar = Calendar.getInstance();
        int year = calendar.get(Calendar.YEAR);
        
        for(int yy = year-1; yy<=year; yy++)
            cbPYear.addItem(yy);
        
        
        // Set the year to current year
        cbPYear.setSelectedIndex(0); 
        
        // Set month
        cbPMonth.setSelectedItem(MONTH[calendar.get(calendar.MONTH)]);
        cbPDay.setSelectedItem(calendar.get(calendar.DAY_OF_MONTH));
    }
    
    private String parseMonth(int month){
        return String.valueOf(month).length()==1? "0"+month : month+"";
    }
    
